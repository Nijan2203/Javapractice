package Basics;

public class ClassH {

	public static void main(String[] args) {
		// program to swap two numbers
		
		int a=1;
		int b=2;
		
// swapping numbers
		System.out.println("Before swapping");
		System.out.println(a);
		System.out.println(b);
		System.out.println("After swapping");
		int temp;
		temp=a;
		a=b;
		b=temp;
		System.out.println(a);
		System.out.println(b);
		
	}

}
