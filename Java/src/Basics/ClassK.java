package Basics;

public class ClassK {

	public static void main(String[] args) {
		// escape sequence
		
		System.out.println("welcome to \"coding\"");
		System.out.println("Hi, \nWelcome to coding");
		
		// to print backlash itself
		System.out.println("\\"); // it will print single back slash
		System.out.println("welcome to \"coding\\");
		System.out.println("c:\\xyz\\abc.txt");
		
	}

}
